medida1=float(input("Primera Medida Clínica (ej. peso en kg): "))
medida2=float(input("Segunda Medida Clínica (ej. factor de dosis): "))

print("Suma")
print(medida1, "+", medida2, medida1+medida2)

print("Resta")
print(medida1, "-", medida2, medida1-medida2)

print("Multiplicacion")
print(medida1, "*", medida2, medida1*medida2)

print("Division")
print(medida1, "/", medida2, medida1/medida2)

print("Potencia")
print(medida1, "**", medida2, medida1**medida2)

print("Modulo")
print(medida1, "%", medida2, medida1%medida2)

print("Exponentes")
print(medida1, "**", medida2, medida1**medida2)